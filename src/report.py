"""HTML report generator for a single sprint.

Produces a self-contained HTML file with inline CSS — no external assets, no
JavaScript. A user can open the file in a browser or email it to stakeholders.
"""

from __future__ import annotations

from dataclasses import asdict
from html import escape
from pathlib import Path
from typing import Iterable

from .metrics import AssigneeLoad, SprintMetrics, StoryTypeBreakdown


STYLE = """
:root { --fg:#1c1917; --muted:#57534e; --accent:#1e40af; --border:#e7e5e4; }
body { font-family: ui-sans-serif, system-ui, -apple-system, Segoe UI, Roboto, sans-serif;
       color: var(--fg); max-width: 860px; margin: 2.5rem auto; padding: 0 1.5rem; }
header h1 { margin: 0 0 0.25rem 0; font-size: 1.75rem; letter-spacing: -0.01em; }
header .meta { color: var(--muted); font-size: 0.9rem; }
h2 { margin-top: 2rem; font-size: 1.2rem; border-bottom: 1px solid var(--border); padding-bottom: 0.35rem; }
table { width: 100%; border-collapse: collapse; margin: 1rem 0; font-size: 0.95rem; }
th, td { text-align: left; padding: 0.5rem 0.6rem; border-bottom: 1px solid var(--border); }
th { font-weight: 600; color: var(--muted); font-size: 0.8rem; text-transform: uppercase; letter-spacing: 0.04em; }
tr:last-child td { border-bottom: 0; }
.kpi-grid { display:grid; grid-template-columns: repeat(4, 1fr); gap: 0.75rem; margin-top: 0.75rem; }
.kpi { background:#fafaf9; border:1px solid var(--border); border-radius: 0.4rem; padding: 0.75rem 0.9rem; }
.kpi .label { font-size: 0.7rem; text-transform: uppercase; letter-spacing: 0.05em; color: var(--muted); }
.kpi .value { font-size: 1.4rem; font-weight: 600; margin-top: 0.2rem; }
footer { margin-top: 3rem; color: var(--muted); font-size: 0.8rem; border-top: 1px solid var(--border); padding-top: 1rem; }
"""


def render_report(
    sprint: dict,
    metrics: SprintMetrics,
    loads: Iterable[AssigneeLoad],
    breakdown: Iterable[StoryTypeBreakdown],
) -> str:
    rows_loads = "".join(
        f"<tr><td>{escape(l.assignee)}</td><td>{l.tickets}</td><td>{l.points}</td></tr>"
        for l in loads
    )
    rows_breakdown = "".join(
        f"<tr><td>{escape(b.story_type)}</td><td>{b.tickets}</td><td>{b.points}</td></tr>"
        for b in breakdown
    )
    pct = f"{metrics.completion_ratio * 100:.1f}%"
    return f"""<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<title>Sprint report — {escape(sprint['sprint_name'])}</title>
<style>{STYLE}</style>
</head>
<body>
<header>
  <h1>{escape(sprint['sprint_name'])}</h1>
  <div class="meta">Board: {escape(sprint['board'])} · {sprint['start_date']} → {sprint['end_date']} · sprint_id {escape(sprint['sprint_id'])}</div>
</header>

<section>
  <h2>Sprint KPIs</h2>
  <div class="kpi-grid">
    <div class="kpi"><div class="label">Completion</div><div class="value">{pct}</div></div>
    <div class="kpi"><div class="label">Points done</div><div class="value">{metrics.completed_points}/{metrics.total_points}</div></div>
    <div class="kpi"><div class="label">Tickets done</div><div class="value">{metrics.completed_ticket_count}/{metrics.ticket_count}</div></div>
    <div class="kpi"><div class="label">In progress pts</div><div class="value">{metrics.in_progress_points}</div></div>
  </div>
</section>

<section>
  <h2>Assignee load</h2>
  <table>
    <thead><tr><th>Assignee</th><th>Tickets</th><th>Story points</th></tr></thead>
    <tbody>{rows_loads}</tbody>
  </table>
</section>

<section>
  <h2>Story-type breakdown</h2>
  <table>
    <thead><tr><th>Story type</th><th>Tickets</th><th>Story points</th></tr></thead>
    <tbody>{rows_breakdown}</tbody>
  </table>
</section>

<footer>
  Generated by the QA automation demo. All data is synthetic — tickets, boards, and people are fictional.
</footer>
</body>
</html>"""


def write_report(output_path: Path, html: str) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(html, encoding="utf-8")
